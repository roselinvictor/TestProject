package operators;

public class Arithmetic {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 2;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		
		System.out.println(100+100);
		System.out.println(100-100);
		System.out.println(100*100);
		System.out.println(100/100);
		
		String s1 = "abc";
		String s2 = "efg";
		System.out.println(s1+s2);
	}

}
